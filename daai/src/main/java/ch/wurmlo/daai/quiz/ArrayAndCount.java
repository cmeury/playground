package ch.wurmlo.daai.quiz;

public class ArrayAndCount {

	private int[] array;
	private long count;
	
	public ArrayAndCount(int[] array, long count) {
		super();
		this.array = array;
		this.count = count;
	}
	
	public int[] getArray() {
		return array;
	}
	public long getCount() {
		return count;
	}
	
	
}
